// Variables 
// are used to store data in a program. They have a name and a type, which determines what kind of data they can hold.
// In C++, variables can be of various data types, such as int, float, double, char, and string.
#include <iostream>
using namespace std;

int main() {
    int age = 20;
    float height = 5.9;
    char initial = 'A';

    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Initial: " << initial << endl;

    return 0;
}