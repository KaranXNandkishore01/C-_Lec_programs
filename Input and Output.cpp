// Input and Output (cin, cout)
// This code demonstrates how to use input and output streams in C++.
#include <iostream>
using namespace std;

int main() {
    int age;
    cout << "Enter your age: ";
    cin >> age;
    cout << "Your age is: " << age << endl;
    return 0;
}
// This code prompts the user to enter their age and then outputs the entered age.
// It uses the standard input (cin) to read the age and standard output (cout) to display it.